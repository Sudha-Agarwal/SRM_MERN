// JavaScript Math example
const num = 25.7;

console.log("Math.round:", Math.round(num));      // 26
console.log("Math.floor:", Math.floor(num));      // 25
console.log("Math.ceil:", Math.ceil(num));        // 26
console.log("Math.sqrt:", Math.sqrt(16));         // 4
console.log("Math.pow:", Math.pow(2, 3));         // 8
console.log("Math.max:", Math.max(4, 10, 3));      // 10
console.log("Math.min:", Math.min(4, 10, 3));      // 3
